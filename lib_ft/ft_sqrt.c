int	ft_sqrt(int nb)
{
	int	i;
	int	sqr;

	i = nb / 2;
	if (nb == 1)
		return (1);
	if (nb >= 65536)
		i = nb / 256;
	if (nb >= 250000)
		i = nb / 500;
	if (nb >= 1000000)
		i = nb / 1000;
	if (nb >= 4000000)
		i = nb / 2000;
	if (nb >= 9000000)
		i = nb / 3000;
	while (i)
	{
		sqr = nb / i;
		if (i * sqr == nb && (i == sqr))
			return (sqr);
		i--;
	}
	return (0);
}
